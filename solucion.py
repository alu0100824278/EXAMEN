#!/usr/bin/python
#! encoding: UTF-8

import modulo

n = int(raw_input('Grado mayor del polinomio 1: '))
m = int(raw_input('Grado mayor del polinomio 2: '))

p1 = [0]*(n+1)
p2 = [0]*(m+1)

for i in range(n+1):
  p1[i] = int(raw_input('Coeficiente de grado (%d) (polinomio 1): ' % (i))) 

for j in range(m+1):
  p2[j] = int(raw_input('Coeficiente de grado (%d) (polinomio 2): ' % (j))) 

modulo.suma(p1,p2)